#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

// ======================
// Get project name from CLI argument
// ======================
const projectName = process.argv[2];

if (!projectName) {
  console.error('❌ Please provide a project name!');
  console.error('   Usage: npx devil-backend-nodejs my-app');
  process.exit(1);
}

const targetDir = path.join(process.cwd(), projectName);
const templateDir = path.join(__dirname, '../templates');

// ======================
// Check if folder already exists
// ======================
if (fs.existsSync(targetDir)) {
  console.error(`❌ Folder "${projectName}" already exists!`);
  process.exit(1);
}

// ======================
// Copy template files recursively
// ======================
const copyRecursive = (src, dest) => {
  const exists = fs.existsSync(src);
  const stats = exists && fs.statSync(src);
  const isDirectory = exists && stats.isDirectory();

  if (isDirectory) {
    fs.mkdirSync(dest, { recursive: true });
    fs.readdirSync(src).forEach((item) => {
      copyRecursive(path.join(src, item), path.join(dest, item));
    });
  } else {
    fs.mkdirSync(path.dirname(dest), { recursive: true });
    fs.copyFileSync(src, dest);
  }
};

// ======================
// Generate package.json for user's project
// ======================
const generatePackageJson = (projectName) => {
  return {
    name: projectName,
    version: '1.0.0',
    description: 'Node.js backend generated by devil-backend-nodejs',
    main: 'server.js',
    scripts: {
      start: 'node server.js',
      dev: 'nodemon server.js',
    },
    dependencies: {
      express: '^4.18.2',
      mongoose: '^7.6.3',
      dotenv: '^16.3.1',
      jsonwebtoken: '^9.0.2',
      bcryptjs: '^2.4.3',
      'express-rate-limit': '^7.1.5',
      'express-validator': '^7.0.1',
      cors: '^2.8.5',
      helmet: '^7.1.0',
      'cookie-parser': '^1.4.6',
      compression: '^1.7.4',
      hpp: '^0.2.3',
      multer: '^1.4.5-lts.1',
      morgan: '^1.10.0',
    },
    devDependencies: {
      nodemon: '^3.0.2',
      'cross-env': '^7.0.3',
    },
  };
};

// ======================
// Start Scaffolding
// ======================
console.log('');
console.log('😈 devil-backend-nodejs');
console.log('='.repeat(50));
console.log(`📁 Creating project: ${projectName}`);
console.log('='.repeat(50));

// Step 1: Copy templates
console.log('📋 Copying template files...');
copyRecursive(templateDir, targetDir);
console.log('✅ Template files copied!');

// Step 2: Generate package.json
console.log('📦 Generating package.json...');
fs.writeFileSync(
  path.join(targetDir, 'package.json'),
  JSON.stringify(generatePackageJson(projectName), null, 2)
);
console.log('✅ package.json created!');

// Step 3: Create empty .env from .env.example
console.log('🔑 Creating .env file...');
const envExample = path.join(targetDir, '.env.example');
const envFile = path.join(targetDir, '.env');
if (fs.existsSync(envExample)) {
  fs.copyFileSync(envExample, envFile);
}
console.log('✅ .env file created!');

// Step 4: Install dependencies
console.log('');
console.log('📦 Installing dependencies...');
console.log('⏳ This may take a minute...');
console.log('');

try {
  execSync('npm install', { cwd: targetDir, stdio: 'inherit' });
  console.log('');
  console.log('='.repeat(50));
  console.log('✅ All dependencies installed!');
} catch (error) {
  console.error('❌ Failed to install dependencies. Run npm install manually.');
}

// Step 5: Success message
console.log('='.repeat(50));
console.log('');
console.log(`🎉 Project "${projectName}" created successfully!`);
console.log('');
console.log('👉 Next steps:');
console.log(`   cd ${projectName}`);
console.log('   Update your .env file with your values');
console.log('   npm run dev');
console.log('');
console.log('😈 Happy coding with devil-backend-nodejs!');
console.log('='.repeat(50));
